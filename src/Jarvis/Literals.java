package Jarvis;

public class Literals {

	public static void main(String[] args) {
		//integer
		int a=10;
		int binary=0b101;
		int octal=012;
		int hex=0x1F;
		
		//float
		double trouble=3.14;
		float dubs=3.14F;
		
		//string
		String greeting="Hello, world";
		
		//character
		char yep='b';
		
		//boolean
		boolean isRaining=true;
		boolean isJavafun=false;
		
		//null
		String novalue=null;
		
		System.out.println("int a "+a);
		System.out.println("int binary "+binary);
		System.out.println("octal "+octal);
		System.out.println("hexadecimal "+hex);
		System.out.println("Double "+trouble);
		System.out.println("float "+dubs);
		System.out.println("String "+greeting);
		System.out.println("Character "+yep);
		System.out.println("Raining "+isRaining);
		System.out.println("java fun "+isJavafun);
		System.out.println("null value "+novalue);
	}

}
